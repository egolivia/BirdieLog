        <div class="card-body">
          <h4 class="card-title">Regel 15 - Erleichterung von losen hinderlichen Naturstoffen und beweglichen Hemmnissen</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Lose hinderliche Naturstoffe</div>
          <div class="text">Zu den losen Naturstoffen zählen Dinge wie Blätter, Äste, Steine, Insekten, Tierkot oder lose Erde, sofern sie nicht fest verwachsen oder verankert sind. Diese dürfen straflos entfernt werden. Sollte sich dabei der Ball bewegen, fällt im Allgemeinen ein Strafschlag an – ausser auf dem Grün, wo das unbeabsichtigte Bewegen des Balls beim Entfernen solcher Stoffe straflos bleibt.</div>
          
          <div class="ueberschrift">Bewegliche Hemmnisse</div>
          <div class="text">Bewegliche Hemmnisse sind zum Beispiel Dosen, Flaschen, Schläger anderer Spieler, Ausrüstungsgegenstände oder Ballmarker. Auch diese dürfen straflos entfernt werden. Sollte sich der Ball dabei bewegen, wird er ohne Strafe an seine ursprüngliche oder eine geschätzte Stelle zurückgelegt.</div>

          <div class="ueberschrift">Ball oder Ballmarker hilft oder behindert Spiel</div>
          <div class="text">Wenn ein Ball oder ein Ballmarker einem anderen Spieler beim Spiel helfen könnte – etwa als Zielhilfe –, darf dieser auf Verlangen entfernt werden. Ebenso darf ein Ball oder Marker auf Wunsch eines anderen Spielers entfernt oder versetzt werden, wenn er das Spiel behindert. In diesem Fall kann der Marker leicht zur Seite verschoben und nach dem Schlag wieder an die ursprüngliche Stelle zurückgesetzt werden.</div>

        </div>
      </div>
    </div>

    <!-- Regel 16 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 16 - Erleichterung bei abnormalen Platzverhältnissen, gefährlichen Tierbedingungen, eingebettetem Ball</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Abnormale Platzverhältnisse</div>
          <div class="text">Zu den abnormalen Platzverhältnissen gehören unter anderem Boden in Ausbesserung, vorübergehende Wasseransammlungen, unbewegliche Hemmnisse wie Wege oder Sprinkler sowie Tierlöcher. Ein Spieler hat Anspruch auf straflose Erleichterung, wenn der Ball in einer solchen Bedingung liegt oder wenn Stand oder Schwung durch sie beeinträchtigt werden. Der Ball muss dann innerhalb einer Schlägerlänge vom nächstgelegenen Punkt vollständiger Erleichterung – jedoch nicht näher zum Loch – gedroppt werden. Keine Erleichterung besteht, wenn lediglich die Sichtlinie beeinträchtigt ist oder der Spieler keinen echten Schlag ausführen würde.</div>
          
          <div class="ueberschrift">Gefährliche Tierbedingungen</div>
          <div class="text">Bei Gefahren durch Tiere, etwa Schlangen, Wespen oder sogar Alligatoren, darf ein Spieler ebenfalls straflos Erleichterung in Anspruch nehmen. In besonderen Fällen kann das Komitee auch Erleichterungsoptionen ausserhalb der normalen Spielbereiche zulassen, gegebenenfalls mit zusätzlichem Strafschlag.</div>

          <div class="ueberschrift">Eingebetteter Ball</div>
          <div class="text">Ein Ball gilt als eingebettet, wenn er sich in seinem eigenen Einschlagloch im Boden befindet und teilweise von Erde bedeckt ist. Dies gilt nicht für Sandflächen ohne Gras. Für einen eingebetteten Ball gibt es eine straflose Erleichterung im allgemeinen Bereich – also zum Beispiel im Fairway oder Rough. Der Ball darf innerhalb einer Schlägerlänge nahe der ursprünglichen Stelle gedroppt werden, sofern dies nicht näher zum Loch erfolgt.</div>

          <div class="ueberschrift">Ball wird aufgenommen zur Überprüfung</div>
          <div class="text">Ein Spieler darf den Ball aufnehmen, um zu überprüfen, ob eine Erleichterung beansprucht werden kann – etwa zur Klärung, ob der Ball eingebettet ist oder sich in Boden in Ausbesserung befindet. Dabei muss die Lage des Balls vorher markiert und der Ball nach der Überprüfung, wenn keine Erleichterung zulässig ist, wieder straflos zurückgelegt werden.</div>

        </div>
      </div>
