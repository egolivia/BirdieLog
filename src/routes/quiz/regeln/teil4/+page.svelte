        <div class="card-body">
          <h4 class="card-title">Regel 12 - Bunker</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Wann liegt ein Ball im Bunker?</div>
          <div class="text">Ein Ball gilt als im Bunker befindlich, wenn er innerhalb der Begrenzungen des Bunkers liegt. Sollte der Ball zwar im Bereich des Bunkers liegen, sich aber beispielsweise auf Gras befinden, das im Bunker wächst, zählt er nicht als im Bunker, wenn er nicht direkt auf dem Sand liegt.</div>
          
          <div class="ueberschrift">Ball im Bunker spielen</div>
          <div class="text">Vor dem Schlag im Bunker dürfen lose Naturstoffe wie Blätter oder kleine Zweige entfernt werden. Der Schläger darf jedoch nicht in den Sand gesetzt werden, um die Bedingungen zu testen – etwa durch einen Probeschwung mit Sandkontakt. Es ist aber erlaubt, sich mit dem Schläger abzustützen, etwa um das Gleichgewicht zu halten. Beim eigentlichen Schlag darf der Schläger den Sand berühren, ohne dass dies zu einer Strafe führt. Sollte jedoch gegen die genannten Vorgaben verstossen werden, wird eine allgemeine Strafe verhängt.</div>

          <div class="ueberschrift">Erleichterung im Bunker</div>
          <div class="text">Wenn ein Spieler eine Erleichterung in Anspruch nehmen möchte, beispielsweise wegen Boden in Ausbesserung, muss der Ball innerhalb des Bunkers gedroppt werden. Möchte der Spieler den Ball ausserhalb des Bunkers droppen, ist dies nur gegen einen Strafschlag erlaubt.</div>

        </div>
      </div>
    </div>

    <!-- Regel 13 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 13 - Grüns</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Erlaubte oder vorgeschriebene Handlungen</div>
          <div class="text">Ein Ball, der sich auf dem Grün befindet, darf jederzeit markiert, aufgenommen und gereinigt werden. Der Spieler darf ausserdem die Spielfläche pflegen, indem er beispielsweise Pitchmarken, alte Lochränder oder Spuren von Schuhen ausbessert. Auch das Entfernen von Sand oder losen Naturstoffen sowie die Reparatur von Schäden durch Tiere oder Turnierausstattung ist erlaubt. Nicht zulässig ist es hingegen, Unebenheiten durch vorherige Putts zu beseitigen, da dies als Verbesserung der Puttlinie gilt und zu einer Strafe führt.</div>
          
          <div class="ueberschrift">Ball bewegt sich nach Markierung</div>
          <div class="text">Wenn sich ein Ball nach dem Markieren und Zurücklegen von selbst bewegt – etwa durch Wind –, wird er nicht erneut an seine ursprüngliche Stelle zurückgelegt, sondern von der neuen Lage aus gespielt. Eine Ausnahme besteht nur, wenn der Spieler selbst den Ball bewegt hat, in diesem Fall muss er zurückgelegt werden.</div>

          <div class="ueberschrift">Die Fahne</div>
          <div class="text">Ein Spieler darf einen Schlag ausführen, während die Fahne im Loch steckt. Trifft der Ball dabei die Fahne, führt dies zu keiner Strafe – selbst wenn der Schlag gezielt darauf ausgerichtet war. Wird die Fahne zuvor entfernt oder von jemandem gehalten, darf sie dennoch versehentlich vom Ball getroffen werden, ohne dass eine Strafe erfolgt, sofern keine Absicht vorlag. Wird die Fahne jedoch absichtlich gehalten und vom Ball getroffen, liegt ein Regelverstoss vor, der bestraft wird.</div>

          <div class="ueberschrift">Ball hängt über dem Loch</div>
          <div class="text">Wenn ein Ball nach einem Putt am Lochrand liegen bleibt, darf der Spieler bis zu zehn Sekunden warten, ob der Ball noch fällt. Rollt der Ball innerhalb dieser Frist ins Loch, zählt er als eingelocht. Fällt er erst nach Ablauf der zehn Sekunden, gilt er ebenfalls als eingelocht, allerdings wird ein Strafschlag hinzugefügt.</div>


