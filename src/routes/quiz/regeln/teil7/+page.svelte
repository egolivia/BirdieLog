        <div class="card-body">
          <h4 class="card-title">Regel 17 - Strafbereiche (Penalty Areas)</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Optionen bei Ball in einer Penalty Area</div>
          <div class="text">Strafbereiche sind durch rote oder gelbe Markierungen gekennzeichnet und umfassen in der Regel Wasserhindernisse, Gräben oder dichtes Gestrüpp. Wenn ein Ball in einem solchen Bereich liegt oder sicher angenommen wird, dass er darin verloren ging, hat der Spieler mehrere Optionen unter Hinzurechnung eines Strafschlags. Bei gelb markierten Bereichen kann der Spieler den Ball spielen, wie er liegt, zur ursprünglichen Stelle zurückkehren und von dort erneut spielen oder sich auf der Verbindungslinie zwischen dem Loch und dem Punkt, an dem der Ball die Grenze des Strafbereichs gekreuzt hat, beliebig weit zurückfallen lassen und dort droppen. Bei rot markierten Bereichen kommt als vierte Option hinzu, innerhalb von zwei Schlägerlängen vom Kreuzungspunkt – jedoch nicht näher zum Loch – zu droppen.</div>
          
          <div class="ueberschrift">Nach dem Schlag aus einer Penalty Area</div>
          <div class="text">Wenn der Ball aus einem Strafbereich gespielt wird und erneut in einer Penalty Area zur Ruhe kommt oder verloren geht, kann der Spieler dieselben Erleichterungsoptionen wie zuvor nutzen – stets unter Hinzurechnung eines weiteren Strafschlags.</div>

          <div class="ueberschrift">Keine weitere Erleichterung innerhalb der Penalty Area</div>
          <div class="text">Innerhalb eines Strafbereichs gibt es keine zusätzliche straflose Erleichterung, etwa bei Boden in Ausbesserung oder unbeweglichen Hemmnissen. Es gelten nur die beschriebenen Optionen mit Strafschlag.</div>

        </div>
      </div>
    </div>

    <!-- Regel 18 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 18 - Schlag und Distanz, verlorener oder ausgerufener Ball, provisorischer Ball</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Schlag- und Distanz-Erleichterung</div>
          <div class="text">Ein Spieler kann jederzeit entscheiden, unter Schlag und Distanz zu verfahren, also den Ball mit einem Strafschlag erneut von der ursprünglichen Stelle zu spielen. Diese Option steht unabhängig vom Grund für den Schlagverlust zur Verfügung.</div>
          
          <div class="ueberschrift">Ball verloren oder im Aus</div>
          <div class="text">Ein Ball gilt als verloren, wenn er nicht innerhalb von drei Minuten gefunden wird. In diesem Fall sowie wenn er sich ausserhalb der Platzgrenzen befindet, muss der Spieler mit einem Strafschlag erneut von der letzten Stelle spielen – das sogenannte Schlag-und-Distanz-Verfahren ist hier verpflichtend.</div>

          <div class="ueberschrift">Provisorischer Ball</div>
          <div class="text">Wenn die Möglichkeit besteht, dass ein Ball verloren gegangen oder im Aus ist – ausgenommen innerhalb eines Strafbereichs –, darf ein Spieler einen provisorischen Ball spielen. Dies muss jedoch ausdrücklich angekündigt werden. Der provisorische Ball wird dann zum Ball im Spiel, wenn der ursprüngliche Ball nicht gefunden wird oder wenn der Spieler ihn weiterspielt, obwohl der ursprüngliche Ball noch hätte gespielt werden können.</div>

        </div>
      </div>
    </div>

    <!-- Regel 19 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 19 - Unspielbarer Ball</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Entscheidung über Unspielbarkeit</div>
          <div class="text">Ob ein Ball als unspielbar erklärt wird, entscheidet ausschliesslich der Spieler. Diese Entscheidung kann an jedem Ort ausserhalb einer Penalty Area getroffen werden.</div>
          
          <div class="ueberschrift">Optionen im allgemeinen Bereich oder auf dem Grün</div>
          <div class="text">Wenn ein Spieler seinen Ball für unspielbar hält, stehen ihm drei Möglichkeiten zur Verfügung, jeweils mit einem Strafschlag. Er kann den Ball von der ursprünglichen Stelle erneut spielen, den Ball auf einer beliebig weit zurückliegenden Linie – durch den Punkt, an dem der Ball lag – droppen oder den Ball innerhalb von zwei Schlägerlängen von seiner Lage – nicht näher zum Loch – droppen.</div>

          <div class="ueberschrift">Ball im Bunker</div>
          <div class="text">Liegt der unspielbare Ball im Bunker, gelten dieselben drei Optionen. Allerdings müssen die Varianten mit Droppen innerhalb des Bunkers erfolgen. Möchte der Spieler den Ball ausserhalb des Bunkers droppen, ist dies nur gegen einen zusätzlichen Strafschlag erlaubt, sodass insgesamt zwei Strafschläge anfallen.</div>

        </div>
      </div>
