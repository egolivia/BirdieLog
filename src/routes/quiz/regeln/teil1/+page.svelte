      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 1 – Das Spiel, Verhalten der Spieler und die Regeln</h4>
          <hr class="custom-divider" />
          <div class="ueberschrift">Das Spiel Golf</div>
          <div class="text">Golf wird in einer Runde von bis zu 18 Löchern gespielt. Jedes Loch beginnt mit einem Schlag vom Abschlag und endet, wenn der Ball eingelocht ist.</div>
          
          <div class="ueberschrift">Verhalten der Spieler</div>
          <div class="text">Von den Spielern wird erwartet, dass sie ehrlich handeln, indem sie etwa Strafen selbst anwenden und die Regeln einhalten. Ebenso sollen sie Rücksicht auf andere nehmen, zügig spielen und niemanden stören. Auch die Pflege des Platzes ist Teil des erwarteten Verhaltens, zum Beispiel durch das Zurücklegen von herausgeschlagenem Rasen oder das Harken der Bunker. Ein Komitee kann darüber hinaus einen Verhaltenskodex aufstellen, in dem bei grobem Fehlverhalten Strafen wie Strafschläge oder eine Disqualifikation vorgesehen sind.</div>

          <div class="ueberschrift">Spiele nach den Regeln</div>
         <div class="text">Jeder Spieler ist selbst verantwortlich, die Regeln korrekt anzuwenden. Bei Unsicherheiten darf er sich an einen Schiedsrichter oder das Komitee wenden. Es gelten festgelegte Strafstufen: Bei kleineren Verstöen wird ein Strafschlag verhängt. Bei schwereren Verstöen droht eine allgemeine Strafe, die im Matchplay zum Verlust des Lochs oder im Zählspiel zu zwei Strafschlägen führt. Besonders schwerwiegende Verstöe führen zur Disqualifikation.</div>
        </div>
      </div>
    </div>

    <!-- Regel 2 -->
   <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 2 - Der Platz</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Platzgrenzen</div>
          <div class="text">Die Grenzen des Platzes werden vom Komitee festgelegt. Alles, was sich auerhalb dieser definierten Fläche befindet, gilt als „aus“.</div>
          
          <div class="ueberschrift">Platzbereiche</div>
          <div class="text">Ein Golfplatz besteht aus fünf klar definierten Bereichen. Dazu zählen der allgemeine Bereich, der unter anderem Fairways und Roughs umfasst, der Abschlag des jeweils zu spielenden Lochs, die sogenannten Penalty Areas wie Wasserhindernisse, die Bunker sowie das Grün des gespielten Lochs.</div>

          <div class="ueberschrift">Objekte und Bedingungen</div>
          <div class="text">Unter bestimmten Bedingungen kann ein Spieler eine kostenlose Erleichterung in Anspruch nehmen. Dies gilt zum Beispiel bei Beeinträchtigungen durch lose Naturstoffe wie Blätter, bei beweglichen Hemmnissen oder bei abnormen Platzverhältnissen wie Boden in Ausbesserung oder Wasser.</div>

          <div class="ueberschrift">Spielverbotszonen</div>
          <div class="text">Bestimmte Bereiche auf dem Platz können als Spielverbotszonen ausgewiesen sein. In diesen Zonen ist das Spielen des Balls nicht erlaubt, und der Spieler muss zwingend eine straflose Erleichterung in Anspruch nehmen.</div>

        </div>
      </div>
    </div>


    <!-- Regel 3 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 3 - Die Spielform</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Grundelemente</div>
          <div class="text">Golf kann in verschiedenen Spielformen gespielt werden. Im Matchplay zählt das Ergebnis jedes Lochs einzeln, das heit, es wird gewertet, ob ein Spieler das Loch gewonnen, verloren oder mit dem Gegner geteilt hat. Im Zählspiel hingegen zählt die Summe aller Schläge über eine oder mehrere Runden hinweg, wobei der Spieler mit dem niedrigsten Gesamtscore gewinnt.</div>
          
          <div class="ueberschrift">Matchplay</div>
          <div class="text">Im Matchplay ist es erlaubt, dass Spieler Schläge, Löcher oder sogar ganze Matches freiwillig an den Gegner abgeben. Es ist erforderlich, dass sich die Spieler über ihre jeweiligen Handicaps und die aktuellen Spielstände informieren. Sie tragen auch die Verantwortung für ihr regelkonformes Verhalten während der gesamten Partie.</div>

          <div class="ueberschrift">Zählspiel</div>
          <div class="text">Im Zählspiel muss der Spieler nach jeder Runde seine Scorekarte korrekt ausfüllen, bestätigen und abgeben. Wenn dabei Fehler unterlaufen oder Angaben fehlen und diese nicht rechtzeitig korrigiert werden, droht die Disqualifikation. Darüber hinaus ist es zwingend erforderlich, jedes Loch korrekt zu beenden, indem der Ball eingelocht wird. Ein Verzicht darauf führt ebenfalls zur Disqualifikation.</div>

        </div>
      </div>
    </div>


    <!-- Regel 3 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 4 - Ausrüstung des Spielers</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Schläger</div>
          <div class="text">Für das Spiel dürfen nur Schläger verwendet werden, die den offiziellen Vorgaben entsprechen. Jeder Spieler darf höchstens 14 Schläger in einer Runde mitführen. Sollte ein Schläger während des Spiels beschädigt werden, darf er nur unter bestimmten Voraussetzungen repariert oder durch einen Ersatzschläger ausgetauscht werden.</div>
          
          <div class="ueberschrift">Bälle</div>
          <div class="text">Auch bei den Bällen gelten klare Regeln. Es dürfen nur solche Bälle verwendet werden, die auf der Liste der zugelassenen Modelle stehen. Ein absichtlich veränderter Ball darf nicht eingesetzt werden und stellt einen Regelversto dar.</div>

          <div class="ueberschrift">Verwendung der Ausrüstung</div>
          <div class="text">Bestimmte Hilfsmittel sind im Spiel erlaubt. Dazu gehören einfache Entfernungsmesser, Informationen über das Wetter, Golfhandschuhe oder Geräte zur Dehnung. Nicht erlaubt ist hingegen die Nutzung von technischen Geräten, die bei der Wahl des Schlägers helfen, von Musik zur Beeinflussung des Spieltempos oder von Trainingshilfen während der Runde.</div>

        </div>
      </div>
    </div>
</div>
