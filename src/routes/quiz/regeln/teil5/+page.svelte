          <!-- Linke Spalte -->
          <div class="row">
            <div class="col-md-6">
              <div class="ueberschrift">Markieren, Aufnehmen und Reinigen des Balls</div>
              <div class="text">Bevor ein Ball aufgenommen wird – etwa zur Identifikation, Reinigung oder zur Überprüfung auf Erleichterung –, muss seine Position durch einen Marker gekennzeichnet werden. Wird der Ball dann aufgenommen, darf er gereinigt werden, es sei denn, dies geschieht im Rahmen einer Überprüfung, ob der Ball eingebettet ist oder ohne dass ein Schlag folgen soll.</div>

              <div class="ueberschrift">Zurücklegen des Balls an die ursprüngliche Stelle</div>
              <div class="text">Wird ein Ball an seine vorherige Position zurückgelegt, kann entweder derselbe oder ein identischer Ersatzball verwendet werden. Der Ball muss exakt an die ursprüngliche Stelle gelegt werden. Ist diese nicht mehr genau erkennbar, muss sie so gut wie möglich geschätzt werden. Der Ball darf dabei nicht gedroppt, sondern muss von Hand aufgelegt werden. Wird dies nicht korrekt ausgeführt, droht die allgemeine Strafe.</div>

              <div class="ueberschrift">Droppen in ein Erleichterungsbereich</div>
              <div class="text">Ein Ball muss immer dann gedroppt werden, wenn eine Regel dies ausdrücklich vorsieht, beispielsweise bei strafloser oder strafbehafteter Erleichterung. Der Spieler muss den Ball selbst aus Kniehöhe fallen lassen, wobei er innerhalb des definierten Erleichterungsbereichs zuerst den Boden berühren muss. Gelingt der Drop nicht korrekt – etwa weil der Ball ausserhalb des Bereichs landet oder weiterrollt –, muss erneut gedroppt werden. Schlägt auch der zweite Versuch fehl, muss der Ball an der richtigen Stelle platziert werden.</div>

              <div class="ueberschrift">Wann ist ein Ball wieder im Spiel?</div>
              <div class="text">Ein Ball gilt wieder als im Spiel, wenn er korrekt gelegt, gedroppt oder zurückgelegt wurde. Befindet sich ein Ball hingegen nicht regelkonform an seinem Platz oder wurde er versehentlich bewegt, gilt er weiterhin als nicht im Spiel.</div>
            </div>

          <!-- Rechte Spalte -->

            <div class="col-md-6">
              
              
              <div class="ueberschrift">Fehlerkorrektur: Ersetzen, Droppen, Platzieren</div>
              <div class="text">Wenn ein Spieler einen Ball falsch ersetzt, platziert oder ausserhalb des erlaubten Bereichs dropt, darf dieser Fehler vor dem nächsten Schlag korrigiert werden. Erfolgt keine Korrektur, gilt der Fehler als geschehen, was in der Regel zu zwei Strafschlägen führt.</div>

              <div class="ueberschrift">Nächster Schlag von derselben Stelle</div>
              <div class="text">Muss ein Ball erneut von seiner ursprünglichen Stelle gespielt werden – etwa nach einem verlorenen Ball oder beim Spielen eines provisorischen Balls –, darf er am Abschlag erneut aufgelegt oder aufgeteet werden. In allen anderen Fällen muss er hingelegt werden, ein Droppen ist in dieser Situation nicht erlaubt.</div>

              <div class="ueberschrift">Spielen von einem falschen Ort</div>
              <div class="text">Ein Ball gilt dann als von einem falschen Ort gespielt, wenn er nicht von einer durch die Regeln zugelassenen Position geschlagen wurde. Im Zählspiel führt dies zur allgemeinen Strafe von zwei Strafschlägen. Bei einem schwerwiegenden Verstoss droht sogar die Disqualifikation, sofern der Schlag nicht ordnungsgemäss wiederholt wird.</div>
