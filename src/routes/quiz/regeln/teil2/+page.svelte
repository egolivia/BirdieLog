     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 6 - Das Spielen eines Lochs</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Beginn des Lochs</div>
          <div class="text">Ein Loch beginnt mit dem Schlag vom richtigen Abschlagsbereich. Wird der Ball ausserhalb des Abschlags gespielt, so zählt der Schlag im Zählspiel nicht und der Spieler erhält zwei Strafschläge. Der Schlag muss wiederholt werden. Im Matchplay kann der Gegner eine Wiederholung verlangen.</div>
          
          <div class="ueberschrift">Spielen vom Abschlagsbereich</div>
          <div class="text">Der Ball muss innerhalb des markierten Abschlagsbereichs liegen, wozu auch der Raum von bis zu zwei Schlägerlängen hinter der Linie zwischen den Markierungen zählt. Fällt der Ball vom Tee oder wird versehentlich getroffen, zählt dies nicht als Schlag und der Ball darf ohne Strafe neu aufgeteet werden.</div>

          <div class="ueberschrift">Verwendeter Ball</div>
          <div class="text">Der Ball, der vom Abschlag gespielt wurde, bleibt im Spiel, es sei denn, er wird durch eine Regel korrekt ersetzt. Ein Ballwechsel ist grundsätzlich nicht erlaubt, ausser wenn dies ausdrücklich durch eine Regel gestattet ist, etwa bei einem verlorenen Ball. Ein nicht erlaubter Wechsel führt im Zählspiel zu zwei Strafschlägen.</div>

          <div class="ueberschrift">Spielreihenfolge im Loch</div>
          <div class="text">Im Matchplay hat der Spieler, der das vorherige Loch gewonnen hat, am nächsten Abschlag die sogenannte „Ehre“ und spielt zuerst. Danach schlägt jeweils derjenige, dessen Ball am weitesten vom Loch entfernt liegt. Ein Verstoss gegen diese Reihenfolge bleibt straffrei, der Gegner kann jedoch eine Wiederholung verlangen. Im Zählspiel gilt dieselbe Reihenfolge, allerdings ohne Strafe bei Abweichungen. Hier ist zudem „Ready Golf“ erlaubt, das heisst, Spieler dürfen ausnahmsweise in abweichender Reihenfolge spielen, wenn dies sicher und zügig möglich ist.</div>

          <div class="ueberschrift">Beenden eines Lochs</div>
          <div class="text">Ein Loch gilt für einen Spieler erst dann als beendet, wenn der Ball eingelocht wurde. Verzichtet ein Spieler im Zählspiel auf das Einlochen, wird er disqualifiziert. Im Matchplay kann ein Loch auch durch den Gegner gegeben werden, wodurch das Einlochen entfällt.</div>

