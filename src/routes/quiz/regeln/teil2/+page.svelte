        <div class="card-body">
          <h4 class="card-title">Regel 5 - Das Spielen der Runde</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Begriffserklärung "Runde"</div>
          <div class="text">Eine Golfrunde besteht aus dem Spielen von bis zu 18 Löchern in der vom Komitee festgelegten Reihenfolge. Wird im Zählspiel ein Stechen erforderlich, zählt dieses als neue Runde. Im Matchplay hingegen gilt eine eventuelle Verlängerung nicht als neue Runde.</div>
          
          <div class="ueberschrift">Üben auf dem Platz vor oder zwischen Runden</div>
          <div class="text">Im Matchplay ist es erlaubt, vor oder zwischen den Runden auf dem Platz zu üben. Im Zählspiel hingegen ist am Turniertag kein Üben auf dem Platz erlaubt – mit Ausnahme von Putten oder Chippen in der Nähe des ersten Abschlags oder auf ausgewiesenen Übungsflächen. Erst nach der letzten Runde des Tages darf wieder geübt werden. Wer diese Regel missachtet, erhält beim ersten Verstoss zwei Strafschläge und wird bei einem weiteren Verstoss disqualifiziert.</div>

          <div class="ueberschrift">Beginn und Ende einer Runde</div>
          <div class="text">Eine Runde beginnt mit dem ersten Schlag am ersten Loch zur vorgegebenen Zeit. Wer bis zu fünf Minuten zu spät kommt oder zu früh startet, erhält zwei Strafschläge. Bei einer Abweichung von mehr als fünf Minuten droht Disqualifikation, sofern keine aussergewöhnlichen Umstände vorliegen. Im Matchplay endet die Runde mit der Entscheidung des Matches, im Zählspiel nach dem Einlochen am letzten Loch – auch wenn danach noch Korrekturen vorgenommen werden.</div>

          <div class="ueberschrift">Spielen in Gruppen</div>
          <div class="text">Im Matchplay müssen Spieler und Gegner gemeinsam spielen. Im Zählspiel ist es verpflichtend, in der vom Komitee zugewiesenen Gruppe zu spielen. Wer davon abweicht, wird disqualifiziert.</div>

          <div class="ueberschrift">Üben während der Runde oder bei Unterbrechung</div>
          <div class="text">Während eines Lochs sind keine Übungsschläge erlaubt, abgesehen von Übungsschwüngen ohne Ballkontakt oder dem Rückspielen eines Balls aus Höflichkeit. Zwischen zwei Löchern ist Putten oder Chippen auf dem zuletzt gespielten Grün, auf Übungsgrüns oder am nächsten Abschlag erlaubt, sofern kein Bunker benutzt wird und der Spielfluss nicht gestört wird. Bei einer Spielunterbrechung darf ausserhalb des Platzes oder an bestimmten, vom Komitee genehmigten Orten geübt werden. Verstösse ziehen eine allgemeine Strafe nach sich.</div>

          <div class="ueberschrift">Unangemessene Verzögerung und zügiges Spiel</div>
          <div class="text">Eine unangemessene Spielverzögerung ist nicht erlaubt, weder innerhalb eines Lochs noch zwischen zwei Löchern. Kurzzeitige Verzögerungen, etwa bei einer Verletzung oder zur Klärung mit einem Schiedsrichter, sind jedoch gestattet. Die Strafen reichen von einem Strafschlag über eine allgemeine Strafe bis hin zur Disqualifikation bei wiederholten Verstössen. Grundsätzlich wird empfohlen, jeden Schlag innerhalb von 40 Sekunden auszuführen. Das Komitee kann zudem verbindliche Zeitvorgaben für die Runde festlegen.</div>
