    <!-- Regel 7 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 7 - Ballsuche</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Faire Suche</div>
          <div class="text">Ein Spieler darf bei der Suche nach seinem Ball den Boden leicht verändern, zum Beispiel Gras niederdrücken oder Sand bewegen, um den Ball zu finden. Dabei ist jedoch darauf zu achten, dass der Platz nicht unnötig beschädigt wird. Solche Handlungen sind nur erlaubt, solange sie vernünftig und dem Auffinden des Balls dienlich sind.</div>
          
          <div class="ueberschrift">Identifikation des Balls</div>
          <div class="text">Wenn der Spieler glaubt, seinen Ball gefunden zu haben, aber sich nicht sicher ist, darf er diesen anheben, um ihn zu identifizieren. Vor dem Anheben muss jedoch die Stelle des Balls mit einem Ballmarker oder einer ähnlichen Markierung gekennzeichnet werden.</div>

          <div class="ueberschrift">Ball anheben zur Identifikation</div>
          <div class="text">Beim Anheben zur Identifikation darf der Ball nur so weit gereinigt werden, wie es für die Erkennung notwendig ist. Wird diese Vorschrift nicht beachtet, erhält der Spieler einen Strafschlag.</div>

          <div class="ueberschrift">Ball versehentlich bewegt</div>
          <div class="text">Bewegt sich der Ball während der Suche versehentlich, liegt keine Strafe vor. In diesem Fall muss der Ball einfach wieder an seine ursprüngliche Stelle zurückgelegt werden.</div>

        </div>
      </div>
    </div>

    <!-- Regel 8 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 8 - Den Platz wie vorgefunden spielen</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Verbesserungen nicht erlaubt</div>
          <div class="text">Ein Grundprinzip des Golfspiels besteht darin, den Platz so zu spielen, wie man ihn vorfindet. Deshalb darf ein Spieler nicht absichtlich Bedingungen verändern, die den nächsten Schlag erleichtern könnten. Verboten ist beispielsweise das Ebnen des Bodens, das Entfernen von Hindernissen – sofern dies nicht durch andere Regeln erlaubt ist – oder das Niederdrücken oder Aufrichten von Gras.</div>
          
          <div class="ueberschrift">Eigene Vorteile durch Veränderungen</div>
          <div class="text">Es ist auch nicht gestattet, durch absichtliche Veränderungen der Umgebung einen Vorteil für den eigenen Ball oder Schlag zu erlangen. Jede Handlung, die darauf abzielt, die Spielsituation zu verbessern, widerspricht den Regeln.</div>

          <div class="ueberschrift">Einflussnahme auf andere Spieler</div>
          <div class="text">Ebenso wenig darf ein Spieler Bedingungen gezielt so verändern, dass dadurch ein anderer Spieler benachteiligt oder dessen Schlag beeinflusst wird. Auch solche Eingriffe sind regelwidrig und können zu einer Strafe führen.</div>

        </div>
      </div>
    </div>

    <!-- Regel 9 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 9 - Den Ball spielen, wie er liegt</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Ball wird gespielt, wie er liegt</div>
          <div class="text">Grundsätzlich muss ein Ball genau von der Stelle gespielt werden, an der er zur Ruhe gekommen ist. Der Spieler darf die Lage des Balls nicht verändern oder verbessern, bevor er seinen Schlag ausführt.</div>
          
          <div class="ueberschrift">Feststellen, ob der Ball bewegt wurde</div>
          <div class="text">Wenn es unklar ist, ob sich der Ball bewegt hat oder wodurch dies geschehen ist, muss der Spieler die Entscheidung auf Grundlage aller verfügbaren Informationen und Fakten treffen.</div>

          <div class="ueberschrift">Bewegung durch natürliche Kräfte</div>
          <div class="text">Wird der Ball durch natürliche Einflüsse wie Wind oder Wasser bewegt, bleibt die neue Position des Balls in der Regel gültig, ohne dass eine Strafe verhängt wird.</div>

          <div class="ueberschrift">Ball vom Spieler bewegt</div>
          <div class="text">Wenn ein Spieler seinen Ball versehentlich bewegt – etwa beim Ansprechen oder durch Berührung – wird in vielen Fällen ein Strafschlag fällig. Danach muss der Ball wieder an die ursprüngliche Position zurückgelegt werden.</div>
          
          <div class="ueberschrift">Gegner bewegt Ball im Matchplay</div>
          <div class="text">Bewegt im Matchplay der Gegner den Ball, liegt in der Regel keine Strafe vor. Der Ball muss jedoch wieder an seine ursprüngliche Stelle zurückgelegt werden.</div>

          <div class="ueberschrift">Einfluss von aussen</div>
          <div class="text">Wird ein Ball durch eine äussere Einwirkung wie ein Tier oder einen Zuschauer bewegt, muss der Ball ebenfalls zurückgelegt werden. In diesem Fall fällt keine Strafe an.</div>

          <div class="ueberschrift">Ballmarker bewegt</div>
          <div class="text">Wenn der Ballmarker versehentlich bewegt wird, gelten dieselben Regeln wie beim Ball: Der Marker wird ohne Strafe an seine ursprüngliche Position zurückgelegt.</div>
          


        </div>
      </div>
    </div>

    <!-- Regel 10 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 10 - Schlag, Rat und Hilfe; Caddies</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Schlag ausführen</div>
          <div class="text">Ein Schlag gilt nur dann als regelkonform, wenn der Ball mit dem Schlägerkopf getroffen wird. Stösse, Schaufelbewegungen oder das Rollen des Balls mit dem Schläger sind nicht erlaubt. Zudem ist es ausschliesslich dem Spieler selbst gestattet, seinen Ball zu schlagen.</div>
          
          <div class="ueberschrift">Rat und Hilfe</div>
          <div class="text">Ein Spieler darf während der Runde weder Rat noch Anweisungen zu Schlägerwahl oder Spielweise von anderen Personen annehmen oder selbst erteilen – ausgenommen davon sind der eigene Caddie oder im Teamspiel der Partner. Erlaubt ist es jedoch, allgemeine Informationen wie etwa zur Windrichtung oder zur Beschaffenheit des Platzes auszutauschen.</div>

          <div class="ueberschrift">Caddies</div>
          <div class="text">Jeder Spieler darf während der Runde einen Caddie haben, der ihn unterstützt. Dieser darf jedoch bestimmte Handlungen nicht ausführen – insbesondere ist es ihm untersagt, sich während des Schlags auf der Spiellinie zu positionieren, um dem Spieler bei der Ausrichtung zu helfen.“</div>

        </div>
      </div>
    </div>

    <!-- Regel 11 -->
     <div class="col-lg-6 mb-4">
      <div class="card w-100 h-100">
        <div class="card-body">
          <h4 class="card-title">Regel 11 - Ball trifft Person, Tier oder Objekt</h4>
          <hr class="custom-divider" />

          <div class="ueberschrift">Ball trifft versehentlich Person oder Einfluss</div>
          <div class="text">Wenn ein Ball während des Spiels in Bewegung ist und dabei versehentlich eine Person, ein Tier oder ein Objekt trifft, liegt grundsätzlich kein Regelverstoss vor. In solchen Fällen wird der Ball so gespielt, wie er nach dem Zusammenstoss liegt, es sei denn, es gelten besondere Ausnahmen, zum Beispiel auf dem Grün.</div>
          
          <div class="ueberschrift">Absichtliches Ablenken oder Stoppen</div>
          <div class="text">Wird der Ball absichtlich von einer Person oder einem Objekt abgelenkt oder gestoppt – etwa um das Spiel zu beeinflussen oder einen Schlag zu verhindern – kann dies zu einer Strafe führen. In manchen Fällen muss der Schlag wiederholt oder der Ball an seinen ursprünglichen Ort zurückgelegt werden.</div>

          <div class="ueberschrift">Absichtliche Veränderung von Bedingungen</div>
          <div class="text">Wenn ein Spieler oder eine andere Person gezielt einen Gegenstand platziert oder eine Massnahme ergreift, um die Bewegung des Balls zu beeinflussen, stellt dies einen Regelverstoss dar, der entsprechend geahndet wird.</div>

        </div>
      </div>
    </div>
